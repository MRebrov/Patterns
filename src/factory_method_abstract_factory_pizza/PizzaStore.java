package factory_method_abstract_factory_pizza;

/**
 *
 * @author A702906
 */
public abstract class PizzaStore {

    public Pizza orderPizza(String type) {
        Pizza pizza;

        pizza = createPizza(type);

        pizza.prepare();
        pizza.bake();
        pizza.cut();
        pizza.box();
        return pizza;
    }
    
    public abstract Pizza createPizza(String item);

}
